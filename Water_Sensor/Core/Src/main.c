int main(void)
{

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* Configure the system clock */
  SystemClock_Config();

  /* Initialize all configured peripherals */
  MX_GPIO_Init();

  /* Initialization LCD */
  LCD_Init();

  /*LCD Entrance Displaying */
  LCD_Initialize();

  while (1)
  {
	  /*check sensor reading or not*/
	  if (HAL_GPIO_ReadPin(GPIOF, GPIO_PIN_8))
	  {
		  LCD_Clear();
		  LCD_Write_String("Sensor Detected");
		  HAL_Delay(500);
	  }
	  else
	  {
		  LCD_Clear();
		  LCD_Write_String ("No Reading Here");
		  HAL_Delay(500);
	  }
  }
}


